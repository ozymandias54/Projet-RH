public void liste(){
    EquipeDAO edao = new EquipeDAO();
        List<Equipe> eq = edao.liste();
        
        for (int i = 0; i < eq.size(); i++) {
            ((DefaultTableModel) tb_Equipe.getModel()).addRow(new Object[]{
                  eq.get(i).getIdEquipe(),
                  eq.get(i).getNomEquipe(),
                  eq.get(i).getChefEquipe(),
                  eq.get(i).getNombreDeMembre()
            });

    }
}

public void vider(){
        txtChefEquipe.setText("");
        txtNomEquipe.setText("");
        DefaultTableModel model = (DefaultTableModel) tb_Equipe.getModel();
        int n = model.getRowCount();
        for (int i = n - 1; i >= 0; --i) {
            model.removeRow(i);
        }
}
