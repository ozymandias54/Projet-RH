    private void btnSupprimer1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnSupprimer1MouseClicked
        int index = tb_Equipe.getSelectedRow();
        if (index==-1){
            JOptionPane.showMessageDialog(this,"Veuillez selectionnez une ligne");
        }
        else {
            new Confirm().affiche("Supprimer","Etes vous sur de vouloir Supprimer!!!", new ActionListener() {
                @Override
                public void actionPerformed(ActionEvent ae) {
                    int index = tb_Equipe.getSelectedRow();
                    int id = (int)tb_Equipe.getValueAt(index,0);
                    EquipeDAO cdao = new EquipeDAO();
                    cdao.supprimer(id);
                    vider();
                    liste();
                }
            });
        }
    }//GEN-LAST:event_btnSupprimer1MouseClicked

    private void btnEnregistrerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEnregistrerActionPerformed
        String chef=txtChefEquipe.getText();
        String nom=txtNomEquipe.getText();
        EquipeDAO edao=new EquipeDAO();
        
        if(nom.equals("")||chef.equals("")){
            JOptionPane.showMessageDialog(this,"Veuillez Remplir tous les champs");
        }
        else if(modifier==false){
            Equipe e=new Equipe(1, chef, nom);
            edao.inserer(e);
            vider();liste();
            frameAjouter.setVisible(false);
        }
        else if(modifier==true){
            int index = tb_Equipe.getSelectedRow();
             int id = (int)tb_Equipe.getValueAt(index,0);
             int membre=(int)tb_Equipe.getValueAt(index,3);
             Equipe e=new Equipe(membre, chef, nom);
            edao.modifier(e, id);
            vider();
            liste();
            frameAjouter.setVisible(false);
        }
    }//GEN-LAST:event_btnEnregistrerActionPerformed

    private void tb_EquipeMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tb_EquipeMouseClicked
        int index = tb_Equipe.getSelectedRow();
        if (index!=-1){
          int id = (int)tb_Equipe.getValueAt(index,0);
            EquipeDAO edao = new EquipeDAO();
            Equipe eq = edao.rechercher(id);
            txtChefEquipe.setText(eq.getChefEquipe());
            txtNomEquipe.setText(eq.getNomEquipe());
        }
    }//GEN-LAST:event_tb_EquipeMouseClicked

