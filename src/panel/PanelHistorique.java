public void liste(){
    HistoriqueDAO udao = new HistoriqueDAO();
    CandidatDAO edao=new CandidatDAO();
        List<Historique> user = udao.liste();
        
        for (int i = 0; i < user.size(); i++) {
            Candidat em=edao.rechercher(user.get(i).getCandidat());
            String nom=em.getNom()+" "+em.getPrenom();
            ((DefaultTableModel) tb_Historique.getModel()).addRow(new Object[]{
                  user.get(i).getIdHistorique(),
                  user.get(i).getPoste(),
                  user.get(i).getDateDebut(),
                  nom
            });

    }
}
public void vider(){
        txtPoste.setText("");
        txtDebut.setDate(null);
        DefaultTableModel model = (DefaultTableModel) tb_Historique.getModel();
        int n = model.getRowCount();
        for (int i = n - 1; i >= 0; --i) {
            model.removeRow(i);
        }
    }
public void combo(){
    CandidatDAO edao=new CandidatDAO();
    List<Candidat> employe=edao.liste();
    for (int i = 0; i < employe.size(); i++){
        String emp=employe.get(i).getIdCandidat()+" "+employe.get(i).getNom()+" "+employe.get(i).getPrenom();
        txtCandidat.addItem(emp);
    }
}

