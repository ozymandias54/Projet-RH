    private void btnSupprimer1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnSupprimer1MouseClicked
        int index = tb_Historique.getSelectedRow();
        if (index==-1){
            JOptionPane.showMessageDialog(this,"Veuillez selectionnez une ligne");
        }
        else {
            new Confirm().affiche("Supprimer","Etes vous sur de vouloir Supprimer!!!", new ActionListener() {
                @Override
                public void actionPerformed(ActionEvent ae) {
                    int index = tb_Historique.getSelectedRow();
                    int id = (int)tb_Historique.getValueAt(index,0);
                    HistoriqueDAO udao = new HistoriqueDAO();
                    udao.supprimer(id);
                    vider();
                    liste();
                }
            });
        }
    }//GEN-LAST:event_btnSupprimer1MouseClicked

    private void btnEnregistrerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEnregistrerActionPerformed
        String poste=txtPoste.getText();
        Date debut=txtDebut.getDate();
        
        String emp=txtCandidat.getSelectedItem().toString();
        int index=emp.indexOf(" ");
        String id=emp.substring(0, index);
        int idCandidat=Integer.parseInt(id);
        
        HistoriqueDAO udao=new HistoriqueDAO();
        
        if(poste.equals("")){
            JOptionPane.showMessageDialog(this,"Veuillez Remplir tous les champs");
        }
        else if(modifier==false){
            Historique u=new Historique(idCandidat, poste, debut);
            udao.inserer(u);
            vider();liste();
            frameAjouter.setVisible(false);
        }
        else if(modifier==true){
            int ligne=tb_Historique.getSelectedRow();
            int idUser =(int)tb_Historique.getValueAt(ligne, 0);
            Historique u=new Historique(idCandidat, poste, debut);
            udao.modifier(u, idUser);
            vider();liste();
            frameAjouter.setVisible(false);
        }
        
    }//GEN-LAST:event_btnEnregistrerActionPerformed

    private void tb_HistoriqueMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tb_HistoriqueMouseClicked
        int index = tb_Historique.getSelectedRow();
        if(index!=-1){
            int id = (int)tb_Historique.getValueAt(index,0);
            HistoriqueDAO udao=new HistoriqueDAO();
            Historique u=udao.rechercher(id);
            txtPoste.setText(u.getPoste());
            txtDebut.setDate(u.getDateDebut());
            
            CandidatDAO edao = new CandidatDAO();
            Candidat employe = edao.rechercher(u.getCandidat());
            String emp=employe.getIdCandidat()+" "+employe.getNom()+" "+employe.getPrenom();
            txtCandidat.setSelectedItem(emp);
        }
    }//GEN-LAST:event_tb_HistoriqueMouseClicked


