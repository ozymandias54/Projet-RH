public void liste(){
    DepartementDAO edao = new DepartementDAO();
        List<Departement> eq = edao.liste();
        
        for (int i = 0; i < eq.size(); i++) {
            ((DefaultTableModel) tb_Departement.getModel()).addRow(new Object[]{
                  eq.get(i).getIdDepartement(),
                  eq.get(i).getNomDepartement(),
                  eq.get(i).getChefDepartement(),
                  eq.get(i).getDescription()
            });

    }
}

public void vider(){
        txtChef.setText("");
        txtNom.setText("");
        txtDescription.setText("");
        DefaultTableModel model = (DefaultTableModel) tb_Departement.getModel();
        int n = model.getRowCount();
        for (int i = n - 1; i >= 0; --i) {
            model.removeRow(i);
        }
}
