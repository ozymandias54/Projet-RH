    private void btnSupprimer1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnSupprimer1MouseClicked
        int index = tb_Departement.getSelectedRow();
        if (index==-1){
            JOptionPane.showMessageDialog(this,"Veuillez selectionnez une ligne");
        }
        else {
            new Confirm().affiche("Supprimer","Etes vous sur de vouloir Supprimer!!!", new ActionListener() {
                @Override
                public void actionPerformed(ActionEvent ae) {
                    int index = tb_Departement.getSelectedRow();
                    int id = (int)tb_Departement.getValueAt(index,0);
                    DepartementDAO cdao = new DepartementDAO();
                    cdao.supprimer(id);
                    vider();
                    liste();
                }
            });
        }
    }//GEN-LAST:event_btnSupprimer1MouseClicked

    private void btnEnregistrerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEnregistrerActionPerformed
        String chef=txtChef.getText();
        String nom=txtNom.getText();
        String description=txtDescription.getText();
        DepartementDAO edao=new DepartementDAO();
        
        if(nom.equals("")||chef.equals("")){
            JOptionPane.showMessageDialog(this,"Veuillez Remplir tous les champs");
        }
        else if(modifier==false){
            Departement e=new Departement(nom, description, chef);
            edao.inserer(e);
            vider();liste();
            frameAjouter.setVisible(false);
        }
        else if(modifier==true){
            int index = tb_Departement.getSelectedRow();
             int id = (int)tb_Departement.getValueAt(index,0);
             Departement e=new Departement(nom, description, chef);
            edao.modifier(e, id);
            vider();
            liste();
            frameAjouter.setVisible(false);
        }
    }//GEN-LAST:event_btnEnregistrerActionPerformed

    private void tb_DepartementMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tb_DepartementMouseClicked
        int index = tb_Departement.getSelectedRow();
        if (index!=-1){
          int id = (int)tb_Departement.getValueAt(index,0);
            DepartementDAO edao = new DepartementDAO();
            Departement eq = edao.rechercher(id);
            txtChef.setText(eq.getChefDepartement());
            txtNom.setText(eq.getNomDepartement());
            txtDescription.setText(eq.getDescription());
        }
    }//GEN-LAST:event_tb_DepartementMouseClicked

