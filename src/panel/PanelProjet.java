public void liste(){
    ProjetDAO edao = new ProjetDAO();
        List<Projet> eq = edao.liste();
        
        for (int i = 0; i < eq.size(); i++) {
            String status="";
            if(eq.get(i).getDateFin()==null) {
                status="Projet en Cours";
            }
            else{
                status="Fini le "+eq.get(i).getDateFin();
            }
            ((DefaultTableModel) tb_Projet.getModel()).addRow(new Object[]{
                    eq.get(i).getIdProjet(),
                    eq.get(i).getNomProjet(),
                    eq.get(i).getDateDebut(),
                    status,
                    eq.get(i).getMission(),
                    eq.get(i).getIdEquipe()
            });

    }
}

public void vider(){
    
        DefaultTableModel model = (DefaultTableModel) tb_Projet.getModel();
        int n = model.getRowCount();
        for (int i = n - 1; i >= 0; --i) {
            model.removeRow(i);
        }
}

public void combo(){
    EquipeDAO edao=new EquipeDAO();
    List<Equipe> eq=edao.liste();
    
    for(int i=0;i<eq.size();i++){
        String e=eq.get(i).getIdEquipe()+" "+eq.get(i).getNomEquipe();
        txtEquipe.addItem(e);
    }
}
