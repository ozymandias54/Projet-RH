    private void jLabel7MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel7MouseClicked
        int index = tb_Projet.getSelectedRow();
        if (index==-1){
            JOptionPane.showMessageDialog(this,"Veuillez selectionnez une ligne");
        }
        else{
            int id = (int)tb_Projet.getValueAt(index,0);
            ProjetDAO edao=new ProjetDAO();
            Projet p=edao.rechercher(id);
            
            if(p.getDateFin()!=null){
                JOptionPane.showMessageDialog(this,"Ce projet est deja Fini");
            }
            else{
                edao.fini(id);
                vider();
                liste();
            }
        }
    }//GEN-LAST:event_jLabel7MouseClicked


