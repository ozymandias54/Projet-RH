    private void btnSupprimer1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnSupprimer1MouseClicked
        int index = tb_Projet.getSelectedRow();
        if (index==-1){
            JOptionPane.showMessageDialog(this,"Veuillez selectionnez une ligne");
        }
        else {
            new Confirm().affiche("Supprimer","Etes vous sur de vouloir Supprimer!!!", new ActionListener() {
                @Override
                public void actionPerformed(ActionEvent ae) {
                    int index = tb_Projet.getSelectedRow();
                    int id = (int)tb_Projet.getValueAt(index,0);
                    ProjetDAO cdao = new ProjetDAO();
                    cdao.supprimer(id);
                    vider();
                    liste();
                }
            });
        }
    }//GEN-LAST:event_btnSupprimer1MouseClicked

    private void btnEnregistrerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEnregistrerActionPerformed
        Date debut=txtDebut.getDate();
        String nom=txtNomProjet.getText();
        String mission=txtMission.getText();
        
        String eq=txtEquipe.getSelectedItem().toString();
        int indexFine=eq.indexOf(" ");
        String indexEq=eq.substring(0, indexFine);
        int idEquipe=Integer.parseInt(indexEq);
        
        ProjetDAO edao=new ProjetDAO();
        
        if(nom.equals("")||mission.equals("")||debut==null){
            JOptionPane.showMessageDialog(this,"Veuillez Remplir tous les champs");
        }
        else if(modifier==false){
            Projet e=new Projet(nom, debut, null, mission, idEquipe);
            edao.inserer(e);
            vider();liste();
            frameAjouter.setVisible(false);
        }
        else if(modifier==true){
            int index = tb_Projet.getSelectedRow();
             int id = (int)tb_Projet.getValueAt(index,0);
             Projet e=new Projet(nom, debut, null, mission, idEquipe);
            edao.modifier(e, id);
            vider();
            liste();
            frameAjouter.setVisible(false);
        }
    }//GEN-LAST:event_btnEnregistrerActionPerformed

    private void tb_ProjetMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tb_ProjetMouseClicked
        int index = tb_Projet.getSelectedRow();
        if (index!=-1){
          int id = (int)tb_Projet.getValueAt(index,0);
            ProjetDAO edao = new ProjetDAO();
            Projet eq = edao.rechercher(id);
            txtNomProjet.setText(eq.getNomProjet());
            txtDebut.setDate(eq.getDateDebut());
            txtMission.setText(eq.getMission());
            
            EquipeDAO eqdao=new EquipeDAO();
            Equipe equipe=eqdao.rechercher(eq.getIdEquipe());
            txtEquipe.setSelectedItem(equipe.getIdEquipe()+" "+equipe.getNomEquipe());
        }
    }//GEN-LAST:event_tb_ProjetMouseClicked

