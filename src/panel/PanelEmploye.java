    private void btnPrimeListeMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnPrimeListeMouseClicked
        int index = tb_Employe.getSelectedRow();
        if (index==-1){
            JOptionPane.showMessageDialog(this,"Veuillez selectionnez un employe");
        }
        else{
            PrimeDAO pdao=new PrimeDAO();
            int id=(int) tb_Employe.getValueAt(index, 0);
            List<Prime> lst=pdao.listePrime(id);
            
            DefaultTableModel model = (DefaultTableModel) tbPrime.getModel();
            int n = model.getRowCount();
            for (int i = n - 1; i >= 0; --i) {
            model.removeRow(i);
            }
            
            if(lst.size()==0){
                JOptionPane.showMessageDialog(this,"Cet Employ√© n a jamais eu de prime");
            }
            else{
                EmployeDAO edao=new EmployeDAO();
                Employe emp=edao.rechercher(id);
                jLabel16.setText("Employ√©: "+emp.getNom()+" "+emp.getPrenom());
                for (int i = 0; i < lst.size(); i++) {
                  ((DefaultTableModel) tbPrime.getModel()).addRow(new Object[]{
                  lst.get(i).getIdPrime(),
                  lst.get(i).getMontant(),
                  lst.get(i).getMotif(),
                  lst.get(i).getStatut()
                });
                }
                framePrimeListe.setVisible(true);
            }
        }
    }//GEN-LAST:event_btnPrimeListeMouseClicked

