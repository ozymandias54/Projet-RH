    private void btnEnregistrerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEnregistrerActionPerformed
        String chef=txtPrenom.getText();
        String nom=txtNom.getText();
        String prenom=txtPrenom.getText();
        Date dateNaiss=txtDateNaissance.getDate();
        String sexe=txtGenre.getSelectedItem().toString();
        boolean genre=false;
        if(sexe.equals("Homme")){
            genre=true;
        }
        double salaire=Double.parseDouble(txtSalaire.getText());
        String eMail=txtEmail.getText();
        String poste=txtPoste.getText();
        
        String dpt=txtDepartement.getSelectedItem().toString();
        int indexFind=dpt.indexOf(" ");
        String indexDpt=dpt.substring(0, indexFind);
        int idDepartement=Integer.parseInt(indexDpt);
        
        String eq=txtEquipe.getSelectedItem().toString();
        int indexFine=eq.indexOf(" ");
        String indexEq=eq.substring(0, indexFine);
        int idEquipe=Integer.parseInt(indexEq);
        
        
        EmployeDAO edao=new EmployeDAO();
        EquipeDAO eqdao=new EquipeDAO();
        
        if(nom.equals("")||chef.equals("")){
            JOptionPane.showMessageDialog(this,"Veuillez Remplir tous les champs");
        }
        else if(modifier==false){
            Employe e=new Employe(nom, prenom, dateNaiss, genre, poste, salaire, eMail, idDepartement, idEquipe);
            edao.inserer(e);
            vider();liste();
            frameAjouter.setVisible(false);
        }
        else if(modifier==true){
            int index = tb_Employe.getSelectedRow();
             int id = (int)tb_Employe.getValueAt(index,0);
             Employe e=new Employe(nom, prenom, dateNaiss, genre, poste, salaire, eMail, idDepartement, idEquipe);
            edao.modifier(e, id);
            vider();
            liste();
            frameAjouter.setVisible(false);
        }
        eqdao.updateMembre();
    }//GEN-LAST:event_btnEnregistrerActionPerformed

    private void tb_EmployeMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tb_EmployeMouseClicked
        int index = tb_Employe.getSelectedRow();
        if (index!=-1){
          int id = (int)tb_Employe.getValueAt(index,0);
            EmployeDAO edao = new EmployeDAO();
            Employe eq = edao.rechercher(id);
            txtPrenom.setText(eq.getPrenom());
            txtNom.setText(eq.getNom());
            txtDateNaissance.setDate(eq.getDateNaissance());
            DepartementDAO ddao=new DepartementDAO();
            Departement dpt=ddao.rechercher(eq.getIdDepartement());
            EquipeDAO eqdao=new EquipeDAO();
            Equipe equipe=eqdao.rechercher(eq.getIdEquipe());
            
            txtDepartement.setSelectedItem(dpt.getIdDepartement()+" "+dpt.getNomDepartement());
            txtEmail.setText(eq.geteMail());
            txtEquipe.setSelectedItem(equipe.getIdEquipe()+" "+equipe.getNomEquipe());
            txtGenre.setSelectedItem("Homme");
            if(eq.isGenre()==false) txtGenre.setSelectedItem("Femme");
            txtPoste.setText(eq.getPoste());
            txtSalaire.setText(eq.getSalaireDeBase()+"");
        }
    }//GEN-LAST:event_tb_EmployeMouseClicked

